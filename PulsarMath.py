def orbital_velocity(height):
    """–†–∞—Å—Å—á–µ—Ç –ø–µ—Ä–≤–æ–π –∫–æ—Å–º–∏—á–µ—Å–∫–æ–π —Å–∫–æ—Ä–æ—Å—Ç–∏"""
    r = R_earth + height
    return math.sqrt(G * M_earth / r)

def rocket_delta_v(Isp, propellant_mass_ratio):
    """–†–∞—Å—Å—á–µ—Ç –∏–∑–º–µ–Ω–µ–Ω–∏—è —Å–∫–æ—Ä–æ—Å—Ç–∏ –ø–æ —Ñ–æ—Ä–º—É–ª–µ –¶–∏–æ–ª–∫–æ–≤—Å–∫–æ–≥–æ"""
    return Isp * 9.81 * math.log(propellant_mass_ratio)

def escape_velocity(planet_mass, planet_radius):
    """–†–∞—Å—Å—á–µ—Ç –≤—Ç–æ—Ä–æ–π –∫–æ—Å–º–∏—á–µ—Å–∫–æ–π —Å–∫–æ—Ä–æ—Å—Ç–∏"""
    return math.sqrt(2 * G * planet_mass / planet_radius)

def stellar_distance(parallax_angle):
    """–†–∞—Å—Å—á–µ—Ç —Ä–∞—Å—Å—Ç–æ—è–Ω–∏—è –¥–æ –∑–≤–µ–∑–¥—ã –ø–æ –ø–∞—Ä–∞–ª–ª–∞–∫—Å—É"""
    if parallax_angle <= 0:
        return float('inf'), float('inf')
    
    parallax_arcsec = parallax_angle / 1000
    distance_parsec = 1 / parallax_arcsec
    distance_ly = distance_parsec * PARSEC_LY
    
    return distance_parsec, distance_ly

def distance_by_apparent_magnitude(apparent_mag, absolute_mag):
    """–†–∞—Å—Å—á–µ—Ç —Ä–∞—Å—Å—Ç–æ—è–Ω–∏—è –ø–æ –∑–≤–µ–∑–¥–Ω–æ–π –≤–µ–ª–∏—á–∏–Ω–µ"""
    distance_parsec = 10 ** ((apparent_mag - absolute_mag + 5) / 5)
    distance_ly = distance_parsec * PARSEC_LY
    
    return distance_parsec, distance_ly

def parachute_calculator(total_mass, target_velocity=6.0, Cd=0.8, altitude=0):
    """–†–∞—Å—á–µ—Ç –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ –ø–∞—Ä–∞—à—é—Ç–∞"""
    rho = RHO_AIR * math.exp(-altitude / 8500)
    area = (2 * total_mass * G_EARTH) / (rho * target_velocity**2 * Cd)
    diameter = 2 * math.sqrt(area / math.pi)
    wing_loading = total_mass / area
    
    if wing_loading < 5:
        parachute_type = "–¢–æ—Ä–º–æ–∑–Ω–æ–π/—Å—Ç–∞–±–∏–ª–∏–∑–∏—Ä—É—é—â–∏–π –ø–∞—Ä–∞—à—é—Ç"
